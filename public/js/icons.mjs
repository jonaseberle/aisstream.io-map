import { map } from './map.mjs';
import { CATEGORIES, shipCategory } from './categories.mjs';
import { filterState } from './visibility.mjs';

export function pixelsPerMeter(lat, zoom) {
  return (256 * Math.pow(2, zoom)) / (40075016.686 * Math.cos(lat * Math.PI / 180));
}

// Shape body in its "pointing up" (angle=0) orientation. The caller rotates
// the whole <svg> via a CSS transform instead of baking rotation into the
// markup, so this never needs to know the current heading.
export function shapeSvgInner(category, color) {
  const withBowDot = (bowY, body) =>
    `${body}<circle cx="8" cy="${bowY}" r="1.8" fill="white" opacity="0.95"/>`;

  switch (category) {
    case 'cargo':
    case 'other':
      return withBowDot(3.5, `<polygon points="8,2 14,18 8,13 2,18" fill="${color}" fill-opacity="0.9"/>`);
    case 'tanker':
      return withBowDot(3.5, `<polygon points="8,2 14,10 8,18 2,10" fill="${color}" fill-opacity="0.9"/>`);
    case 'passenger':
      return withBowDot(3.5, `<polygon points="8,2 15,16 8,11 1,16" fill="${color}" fill-opacity="0.9"/>`);
    case 'sailing':
      return withBowDot(3.5, `<polygon points="8,2 11,18 8,14 5,18" fill="${color}" fill-opacity="0.9"/>`);
    case 'service':
      return withBowDot(3.5, `<polygon points="8,2 14,7 12,18 4,18 2,7" fill="${color}" fill-opacity="0.9"/>`);
    case 'hsc':
      return withBowDot(2.5, `<polygon points="8,1 11,19 8,15 5,19" fill="${color}" fill-opacity="0.9"/>`);
    case 'military':
      return withBowDot(4.5, `<rect x="3" y="3" width="10" height="14" rx="1" fill="${color}" fill-opacity="0.9"/>`);
    case 'fishing':
      // No heading — circle with cross
      return `<circle cx="8" cy="10" r="6" fill="${color}" fill-opacity="0.85"/>
              <line x1="8" y1="4" x2="8" y2="16" stroke="rgba(0,0,0,0.4)" stroke-width="1.5"/>
              <line x1="2" y1="10" x2="14" y2="10" stroke="rgba(0,0,0,0.4)" stroke-width="1.5"/>`;
    case 'unknown':
    default:
      // No static data — plain circle, no direction
      return `<circle cx="8" cy="10" r="6" fill="${color}" fill-opacity="0.55" stroke="${color}" stroke-width="1.2"/>`;
  }
}

// y-coordinate (in the 16x20 small-icon box) where each category's bow tip
// indicator sits; speed dots trail forward from this point. Categories with
// no heading (fishing, unknown) are omitted — they get no speed dots.
const BOW_Y = {
  cargo: 3.5, other: 3.5, tanker: 3.5, passenger: 3.5, sailing: 3.5,
  service: 3.5, hsc: 2.5, military: 4.5,
};
// Same vertex coordinates as the polygons/rect drawn in shapeSvgInner, kept
// separately so we can compute each icon's (unrotated) bounding box — used
// only to size the divIcon's canvas/click area tightly. The actual rendered
// shape, generated by shapeSvgInner, is untouched.
const CATEGORY_POLY_POINTS = {
  cargo:     [[8, 2], [14, 18], [8, 13], [2, 18]],
  other:     [[8, 2], [14, 18], [8, 13], [2, 18]],
  tanker:    [[8, 2], [14, 10], [8, 18], [2, 10]],
  passenger: [[8, 2], [15, 16], [8, 11], [1, 16]],
  sailing:   [[8, 2], [11, 18], [8, 14], [5, 18]],
  service:   [[8, 2], [14, 7], [12, 18], [4, 18], [2, 7]],
  hsc:       [[8, 1], [11, 19], [8, 15], [5, 19]],
  military:  [[3, 3], [13, 3], [13, 17], [3, 17]],
};
export const SPEED_DOT_SPACING = 3; // px between speed dots, 1 dot per knot at zoom >= 11

function pointsBounds(points, margin = 0) {
  let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
  for (const [x, y] of points) {
    if (x < minX) minX = x; if (x > maxX) maxX = x;
    if (y < minY) minY = y; if (y > maxY) maxY = y;
  }
  return { minX: minX - margin, maxX: maxX + margin, minY: minY - margin, maxY: maxY + margin };
}
function unionBounds(a, b) {
  return {
    minX: Math.min(a.minX, b.minX), maxX: Math.max(a.maxX, b.maxX),
    minY: Math.min(a.minY, b.minY), maxY: Math.max(a.maxY, b.maxY),
  };
}
// Rotates the vector (dx,dy) by angleDeg (clockwise, matching SVG's rotate()).
function rotateVec(dx, dy, angleDeg) {
  const a = (angleDeg || 0) * Math.PI / 180;
  const cos = Math.cos(a), sin = Math.sin(a);
  return [dx * cos - dy * sin, dx * sin + dy * cos];
}

// Below zoom 11, halve the dot count for every zoom step below it, so the
// trail doesn't dominate the (now visually larger) icon at low zoom.
export function speedDotZoomFactor() {
  const zoom = map.getZoom();
  return zoom < 11 ? Math.pow(2, 11 - zoom) : 1;
}
export function speedDotCount(sog) {
  if (!Number.isFinite(sog)) return 0;
  return Math.floor(sog / speedDotZoomFactor());
}

// Speed-dot trail anchored exactly at the bow tip's rendered position (which
// depends on headingAngle), then extending in dotsAngle's direction (COG can
// differ from heading). Computed as absolute offsets from the pivot so the
// dots stay glued to the tip regardless of how far heading/COG diverge —
// they're drawn in a non-rotating layer rather than a CSS-rotated one, since
// that's the only way to compose "start exactly at the (rotated) tip, then
// point a different way" without risking clipping from nested transforms.
function speedDotPoints(pivot, bowOffset, headingAngle, dotsAngle, count) {
  if (count <= 0) return [];
  const [bowDx, bowDy] = rotateVec(0, bowOffset, headingAngle);
  const [dirX, dirY] = rotateVec(0, -1, dotsAngle); // unit vector pointing towards COG
  const pts = [];
  for (let i = 1; i <= count; i++) {
    const d = i * SPEED_DOT_SPACING;
    pts.push([pivot[0] + bowDx + d * dirX, pivot[1] + bowDy + d * dirY]);
  }
  return pts;
}
function speedDotsSvg(points) {
  return points.map(([x, y]) => `<circle cx="${x.toFixed(1)}" cy="${y.toFixed(1)}" r="1" fill="white" opacity="0.9"/>`).join('');
}

// Floating (stale-position) overlay: a translucent dark badge with a "!"
// glyph, centered on the icon's anchor point. Lives in the non-rotating
// layer, so it stays upright regardless of the ship's heading.
function floatingMarkSvg(cx, cy) {
  return `<g><circle cx="${cx.toFixed(1)}" cy="${cy.toFixed(1)}" r="4.5" fill="#1a1a1a" opacity="0.75" stroke="white" stroke-width="0.6"/><text x="${cx.toFixed(1)}" y="${(cy + 2.6).toFixed(1)}" font-size="7" font-weight="bold" fill="white" text-anchor="middle" font-family="sans-serif">!</text></g>`;
}

// A single rotating layer (the heading-dependent body, turned via a CSS
// transform on its own <svg>) stacked with a static layer (anything that
// must stay upright/fixed, e.g. the speed-dot trail and the floating "!"
// badge), both the same tight size.
function layeredIcon({ width, height, anchorX, anchorY, angle, bodyOpacity, bodyContent, staticContent }) {
  const w = width.toFixed(1), h = height.toFixed(1);
  const ax = anchorX.toFixed(1), ay = anchorY.toFixed(1);
  const rotatingSvg = `<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}" style="position:absolute;left:0;top:0;transform:rotate(${angle}deg);transform-origin:${ax}px ${ay}px;"><g opacity="${bodyOpacity}">${bodyContent}</g></svg>`;
  const staticSvg = staticContent
    ? `<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}" style="position:absolute;left:0;top:0;">${staticContent}</svg>`
    : '';
  return L.divIcon({ html: rotatingSvg + staticSvg, iconSize: [width, height], iconAnchor: [anchorX, anchorY], className: 'ship-divicon' });
}

// headingAngle orients the hull/body shape; dotAngle orients the speed-dot
// trail separately (COG — direction of travel — can differ from heading,
// e.g. when crabbing in current/wind). The dots are always anchored at the
// body's actual (heading-rotated) bow tip, then extend towards dotAngle.
export function shipIcon(headingAngle, dotAngle, sog, typeCode, dim, isFloating) {
  const cat = shipCategory(typeCode);
  const color = CATEGORIES[cat].color;
  const dotCount = speedDotCount(sog);
  const bodyOpacity = isFloating ? 0.4 : 1;
  const angle = Number.isFinite(headingAngle) ? headingAngle : 0;
  const dotsAngle = Number.isFinite(dotAngle) ? dotAngle : angle;

  if (dim) {
    const ppm = pixelsPerMeter(map.getCenter().lat, map.getZoom());
    const A = (dim.A || 0) * ppm, B = (dim.B || 0) * ppm;
    const C = (dim.C || 0) * ppm, D = (dim.D || 0) * ppm;
    const lengthPx = A + B, beamPx = C + D;
    if (lengthPx >= 8 && beamPx >= 2) {
      // Draw the hull symmetric left/right (half the reported beam on each
      // side), rather than skewed by the GPS antenna's actual port/starboard offset.
      const halfBeam = beamPx / 2;
      const half = lengthPx / 2; // hull half-length from the middle — the pivot/anchor below, not the antenna
      const bowLen = Math.min(beamPx * 0.7, half); // triangular bow section height

      // The GPS antenna sits wherever the AIS dimension report says, offset
      // from the hull's middle (the rotation pivot — see shipMiddlePosition
      // in geo.mjs, which the marker itself is now positioned at).
      const antennaX = (C - D) / 2; // +starboard
      const antennaY = (A - B) / 2; // +stern

      // Tight bounding box of the hull alone, in its unrotated "pointing up"
      // orientation — safe for any headingAngle since the body's whole <svg>
      // layer is what gets CSS-rotated. The speed dots are placed via
      // explicit trig (see below) and are NOT angle-independent, so their
      // actual extent is unioned in afterwards.
      const localHullPts = [[0, -half], [halfBeam, -half + bowLen], [halfBeam, half], [-halfBeam, half], [-halfBeam, -half + bowLen]];
      const crossR = 3;
      let bounds = pointsBounds(localHullPts, 2); // margin for stroke width + bow-circle radius
      if (filterState.showAntenna) bounds = unionBounds(bounds, pointsBounds([[antennaX, antennaY]], crossR));
      if (isFloating) bounds = unionBounds(bounds, { minX: -4.5, maxX: 4.5, minY: -4.5, maxY: 4.5 });

      // Speed dots, anchored at the bow tip's rotated position, extending
      // towards dotsAngle — computed relative to the middle pivot (0,0) for
      // now; shifted into the final canvas once its size is known below.
      const dotPtsLocal = speedDotPoints([0, 0], -half, angle, dotsAngle, dotCount);
      if (dotPtsLocal.length) bounds = unionBounds(bounds, pointsBounds(dotPtsLocal, 1.5));

      const width = bounds.maxX - bounds.minX, height = bounds.maxY - bounds.minY;
      const ox = -bounds.minX, oy = -bounds.minY; // the hull's middle (== marker's latlng) within the tight canvas

      const pts = [
        `${ox.toFixed(1)},${(oy - half).toFixed(1)}`,           // bow tip
        `${(ox + halfBeam).toFixed(1)},${(oy - half + bowLen).toFixed(1)}`, // starboard shoulder
        `${(ox + halfBeam).toFixed(1)},${(oy + half).toFixed(1)}`,     // starboard stern
        `${(ox - halfBeam).toFixed(1)},${(oy + half).toFixed(1)}`,     // port stern
        `${(ox - halfBeam).toFixed(1)},${(oy - half + bowLen).toFixed(1)}`, // port shoulder
      ].join(' ');
      // GPS antenna crosshair — a fixed point on the hull, so (unlike before,
      // when it sat right at the non-rotating anchor) it now lives in the
      // rotating body layer and swings around the middle with the hull.
      const antennaMark = filterState.showAntenna
        ? `<line x1="${(ox + antennaX - crossR).toFixed(1)}" y1="${(oy + antennaY).toFixed(1)}" x2="${(ox + antennaX + crossR).toFixed(1)}" y2="${(oy + antennaY).toFixed(1)}" stroke="magenta" stroke-width="1"/><line x1="${(ox + antennaX).toFixed(1)}" y1="${(oy + antennaY - crossR).toFixed(1)}" x2="${(ox + antennaX).toFixed(1)}" y2="${(oy + antennaY + crossR).toFixed(1)}" stroke="magenta" stroke-width="1"/>`
        : '';
      const bodyContent = `<polygon points="${pts}" fill="${color}" fill-opacity="0.85" stroke="rgba(255,255,255,0.4)" stroke-width="0.8"/><circle cx="${ox.toFixed(1)}" cy="${(oy - half).toFixed(1)}" r="1.5" fill="white" opacity="0.9"/>${antennaMark}`;
      const speedDots = speedDotsSvg(speedDotPoints([ox, oy], -half, angle, dotsAngle, dotCount));
      const staticContent = speedDots + (isFloating ? floatingMarkSvg(ox, oy) : '');
      return layeredIcon({ width, height, anchorX: ox, anchorY: oy, angle, bodyOpacity, bodyContent, staticContent });
    }
  }

  const inner = shapeSvgInner(cat, color);
  const bowY = BOW_Y[cat];

  if (bowY == null) {
    // No heading (fishing/unknown) — nothing rotates, so a single plain
    // <svg> is enough. Tight, fixed bounds around the unrotated circle.
    const bounds = { minX: 8 - 6.6, maxX: 8 + 6.6, minY: 10 - 6.6, maxY: 10 + 6.6 };
    const width = bounds.maxX - bounds.minX, height = bounds.maxY - bounds.minY;
    const anchorX = 8 - bounds.minX, anchorY = 10 - bounds.minY;
    const mark = isFloating ? floatingMarkSvg(anchorX, anchorY) : '';
    const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${width.toFixed(1)}" height="${height.toFixed(1)}" viewBox="0 0 ${width.toFixed(1)} ${height.toFixed(1)}"><g opacity="${bodyOpacity}" transform="translate(${(-bounds.minX).toFixed(1)},${(-bounds.minY).toFixed(1)})">${inner}</g>${mark}</svg>`;
    return L.divIcon({ html: svg, iconSize: [width, height], iconAnchor: [anchorX, anchorY], className: 'ship-divicon' });
  }

  // Tight bounding box of the category shape (+ bow dot) in its unrotated
  // orientation, around the (8,10) pivot used by shapeSvgInner — safe for
  // any headingAngle. Speed dots are placed via explicit trig and unioned in.
  const bounds0 = pointsBounds([...(CATEGORY_POLY_POINTS[cat] || []), [8, bowY]], 2);
  const dotPtsLocal = speedDotPoints([8, 10], bowY - 10, angle, dotsAngle, dotCount);
  const bounds = dotPtsLocal.length ? unionBounds(bounds0, pointsBounds(dotPtsLocal, 1.5)) : bounds0;
  const width = bounds.maxX - bounds.minX, height = bounds.maxY - bounds.minY;
  const anchorX = 8 - bounds.minX, anchorY = 10 - bounds.minY; // pivot (8,10)'s position within the tight canvas

  const dx = (-bounds.minX).toFixed(1), dy = (-bounds.minY).toFixed(1);
  const bodyContent = `<g transform="translate(${dx},${dy})">${inner}</g>`;
  const speedDots = speedDotsSvg(speedDotPoints([anchorX, anchorY], bowY - 10, angle, dotsAngle, dotCount));
  const staticContent = speedDots + (isFloating ? floatingMarkSvg(anchorX, anchorY) : '');
  return layeredIcon({ width, height, anchorX, anchorY, angle, bodyOpacity, bodyContent, staticContent });
}
